/*
 * multipart.c — S3 Multipart Upload (CreateMultipartUpload / UploadPart /
 *               CompleteMultipartUpload / AbortMultipartUpload /
 *               ListParts / ListMultipartUploads).
 *
 * Design overview
 * ---------------
 * Parts are staged as files named "part.<N>" inside a hidden working directory
 * alongside the target object:
 *
 *   <root>/<bucket>/<key>            ← final object (created at Complete time)
 *   <root>/<bucket>/.<key>.mpu-<id>/ ← staging dir
 *   <root>/<bucket>/.<key>.mpu-<id>/part.1
 *   <root>/<bucket>/.<key>.mpu-<id>/part.2
 *   ...
 *
 * CompleteMultipartUpload streams part.1, part.2, … in order into a temp file
 * then atomically renames it to the final path.  The staging dir is then
 * deleted with a safe opendir/unlinkat recursive helper — no shell subprocess.
 *
 * AbortMultipartUpload removes the staging dir in the same safe way.
 *
 * Security notes
 * --------------
 * - partNumber is range-checked (1–10000) and validated to be a decimal
 *   integer before it appears in any path — no shell metachar risk.
 * - All filesystem paths go through xrootd_open_confined_canon /
 *   xrootd_mkdir_confined_canon / xrootd_rename_confined_canon to prevent
 *   path traversal.
 * - The recursive delete helper (mpu_rmdir_recursive) is self-contained in
 *   this file, uses opendir/unlinkat internally, and enforces path confinement
 *   via xrootd_unlink_confined_canon.
 */

#include "s3.h"

#include <dirent.h>
#include <fcntl.h>
#include <limits.h>
#include <string.h>
#include <sys/stat.h>
#include <unistd.h>

/* Maximum valid S3 part number (AWS limit). */
#define MPU_MAX_PART_NUMBER  10000

/* -------------------------------------------------------------------------
 * Query-string helpers
 * ---------------------------------------------------------------------- */

/*
 * s3_has_query_flag — return 1 if <key> appears as a bare flag (no '=') in
 * the request query string, 0 otherwise.
 */
int
s3_has_query_flag(ngx_http_request_t *r, const char *key)
{
    u_char *p, *last, *amp;
    size_t  keylen = ngx_strlen(key);

    if (r->args.len == 0) {
        return 0;
    }

    p    = r->args.data;
    last = p + r->args.len;

    while (p < last) {
        amp = ngx_strlchr(p, last, '&');
        if (amp == NULL) {
            amp = last;
        }

        if ((size_t)(amp - p) == keylen
            && ngx_strncmp(p, key, keylen) == 0)
        {
            return 1;
        }

        p = amp + 1;
    }

    return 0;
}

/*
 * s3_get_query_param — copy the value of query parameter <key> into <out>.
 * Returns 1 on success, 0 if the key is absent or the value is empty.
 */
int
s3_get_query_param(ngx_http_request_t *r, const char *key,
                   char *out, size_t outsz)
{
    u_char *p, *last, *amp;
    size_t  keylen = ngx_strlen(key);
    size_t  val_len;

    if (r->args.len == 0) {
        return 0;
    }

    p    = r->args.data;
    last = p + r->args.len;

    while (p < last) {
        amp = ngx_strlchr(p, last, '&');
        if (amp == NULL) {
            amp = last;
        }

        if ((size_t)(amp - p) > keylen
            && p[keylen] == '='
            && ngx_strncmp(p, key, keylen) == 0)
        {
            val_len = (size_t)(amp - (p + keylen + 1));
            if (val_len == 0) {
                return 0;           /* key present but value empty */
            }
            if (val_len >= outsz) {
                val_len = outsz - 1;
            }
            ngx_memcpy(out, p + keylen + 1, val_len);
            out[val_len] = '\0';
            return 1;
        }

        p = amp + 1;
    }

    return 0;
}

/* -------------------------------------------------------------------------
 * MPU path helpers
 * ---------------------------------------------------------------------- */

/*
 * s3_get_mpu_dir — build the staging directory path for an in-progress MPU.
 *
 * Given fs_path = "/export/bucket/subdir/key.root" and upload_id "abc123",
 * produces "/export/bucket/subdir/.key.root.mpu-abc123".
 *
 * The leading '.' keeps the directory hidden from S3 LIST responses and
 * ordinary directory listings.
 */
void
s3_get_mpu_dir(const char *fs_path, const char *upload_id,
               char *out_dir, size_t outsz)
{
    const char *last_slash = strrchr(fs_path, '/');
    u_char     *p;

    if (last_slash != NULL) {
        size_t dir_prefix_len = (size_t)(last_slash - fs_path) + 1; /* includes '/' */
        p = ngx_snprintf((u_char *)out_dir, outsz - 1,
                         "%*s.%s.mpu-%s",
                         (int)dir_prefix_len, fs_path,
                         last_slash + 1, upload_id);
    } else {
        p = ngx_snprintf((u_char *)out_dir, outsz - 1,
                         ".%s.mpu-%s", fs_path, upload_id);
    }
    *p = '\0';
}

/*
 * mpu_validate_upload_id — return 1 if upload_id contains only safe hex
 * characters (as generated by s3_handle_multipart_initiate).  Rejects any
 * value that could affect path construction.
 */
static int
mpu_validate_upload_id(const char *upload_id)
{
    const char *p = upload_id;

    if (*p == '\0') {
        return 0;
    }
    for (; *p != '\0'; p++) {
        if (!((*p >= '0' && *p <= '9') || (*p >= 'a' && *p <= 'f'))) {
            return 0;
        }
    }
    return 1;
}

/* -------------------------------------------------------------------------
 * Safe recursive directory removal — no shell subprocess
 * ---------------------------------------------------------------------- */

/*
 * mpu_rmdir_recursive — remove <path> and all its contents using
 * opendir/unlinkat.  Path confinement is enforced via
 * xrootd_unlink_confined_canon for every remove call.
 *
 * Returns 0 on success, -1 on error (errno set).
 */
static int
mpu_rmdir_recursive(ngx_log_t *log, const char *root_canon, const char *path)
{
    DIR           *dp;
    struct dirent *de;
    char           child[PATH_MAX];
    struct stat    sb;
    int            rc = 0;

    dp = opendir(path);
    if (dp == NULL) {
        if (errno == ENOENT) {
            return 0;               /* already gone — treat as success */
        }
        ngx_log_error(NGX_LOG_ERR, log, errno,
                      "s3 mpu_rmdir_recursive: opendir(\"%s\") failed", path);
        return -1;
    }

    while ((de = readdir(dp)) != NULL) {
        /* skip . and .. */
        if (de->d_name[0] == '.'
            && (de->d_name[1] == '\0'
                || (de->d_name[1] == '.' && de->d_name[2] == '\0')))
        {
            continue;
        }

        if ((size_t)snprintf(child, sizeof(child), "%s/%s",
                             path, de->d_name) >= sizeof(child))
        {
            ngx_log_error(NGX_LOG_ERR, log, 0,
                          "s3 mpu_rmdir_recursive: path too long: \"%s/%s\"",
                          path, de->d_name);
            rc = -1;
            break;
        }

        if (lstat(child, &sb) != 0) {
            if (errno == ENOENT) {
                continue;           /* removed by another worker — skip */
            }
            ngx_log_error(NGX_LOG_ERR, log, errno,
                          "s3 mpu_rmdir_recursive: lstat(\"%s\") failed", child);
            rc = -1;
            break;
        }

        if (S_ISDIR(sb.st_mode)) {
            if (mpu_rmdir_recursive(log, root_canon, child) != 0) {
                rc = -1;
                break;
            }
        } else {
            if (xrootd_unlink_confined_canon(log, root_canon, child, 0) != 0) {
                ngx_log_error(NGX_LOG_ERR, log, errno,
                              "s3 mpu_rmdir_recursive: unlink(\"%s\") failed",
                              child);
                rc = -1;
                break;
            }
        }
    }

    closedir(dp);

    if (rc == 0) {
        if (xrootd_unlink_confined_canon(log, root_canon, path, 1) != 0) {
            ngx_log_error(NGX_LOG_ERR, log, errno,
                          "s3 mpu_rmdir_recursive: rmdir(\"%s\") failed", path);
            rc = -1;
        }
    }

    return rc;
}

/* -------------------------------------------------------------------------
 * POST /bucket/key?uploads  →  InitiateMultipartUpload
 * ---------------------------------------------------------------------- */

ngx_int_t
s3_handle_multipart_initiate(ngx_http_request_t *r,
                              const char *fs_path,
                              ngx_http_s3_loc_conf_t *cf,
                              const char *key_str)
{
    char        upload_id[25];  /* 8+8+8 hex digits + NUL */
    char        mpu_dir[PATH_MAX];
    char        xml_buf[512];
    ngx_buf_t  *b;
    ngx_chain_t out;
    struct timeval tv;
    u_char     *p;
    size_t      xml_len;

    /* Generate a simple opaque upload ID from timestamp + usec + pid.
     * Three 8-hex fields gives 96 bits of uniqueness — sufficient for a
     * staging token that is only valid for the lifetime of the upload. */
    ngx_gettimeofday(&tv);
    p = ngx_snprintf((u_char *)upload_id, sizeof(upload_id) - 1,
                     "%08xl%08xl%08xl",
                     (unsigned long)tv.tv_sec,
                     (unsigned long)tv.tv_usec,
                     (unsigned long)ngx_pid);
    *p = '\0';

    /* Trim the format suffix characters ('l') that ngx_snprintf emits for %l */
    /* Simpler: just use fixed hex with snprintf since upload_id is not a path */
    snprintf(upload_id, sizeof(upload_id), "%08lx%08lx%08lx",
             (unsigned long)tv.tv_sec,
             (unsigned long)tv.tv_usec,
             (unsigned long)ngx_pid);

    s3_get_mpu_dir(fs_path, upload_id, mpu_dir, sizeof(mpu_dir));

    if (xrootd_mkdir_confined_canon(r->connection->log, cf->root_canon,
                                    mpu_dir, 0700) != 0)
    {
        ngx_log_error(NGX_LOG_ERR, r->connection->log, errno,
                      "s3 initiate_mpu: mkdir(\"%s\") failed", mpu_dir);
        XROOTD_S3_METRIC_INC(events_total[XROOTD_S3_EVENT_INTERNAL_ERROR]);
        return NGX_HTTP_INTERNAL_SERVER_ERROR;
    }

    /* Build the XML response body */
    xml_len = (size_t)snprintf(xml_buf, sizeof(xml_buf),
        "<?xml version=\"1.0\" encoding=\"UTF-8\"?>\n"
        "<InitiateMultipartUploadResult"
        " xmlns=\"http://s3.amazonaws.com/doc/2006-03-01/\">\n"
        "  <Bucket>%s</Bucket>\n"
        "  <Key>%s</Key>\n"
        "  <UploadId>%s</UploadId>\n"
        "</InitiateMultipartUploadResult>\n",
        cf->bucket.data ? (const char *)cf->bucket.data : "default",
        key_str,
        upload_id);

    if (xml_len >= sizeof(xml_buf)) {
        XROOTD_S3_METRIC_INC(events_total[XROOTD_S3_EVENT_INTERNAL_ERROR]);
        return NGX_HTTP_INTERNAL_SERVER_ERROR;
    }

    b = ngx_create_temp_buf(r->pool, xml_len);
    if (b == NULL) {
        XROOTD_S3_METRIC_INC(events_total[XROOTD_S3_EVENT_INTERNAL_ERROR]);
        return NGX_HTTP_INTERNAL_SERVER_ERROR;
    }
    ngx_memcpy(b->pos, xml_buf, xml_len);
    b->last    = b->pos + xml_len;
    b->last_buf = 1;

    out.buf  = b;
    out.next = NULL;

    r->headers_out.status           = NGX_HTTP_OK;
    r->headers_out.content_length_n = (off_t)xml_len;
    ngx_http_send_header(r);
    return ngx_http_output_filter(r, &out);
}

/* -------------------------------------------------------------------------
 * DELETE /bucket/key?uploadId=<id>  →  AbortMultipartUpload
 * ---------------------------------------------------------------------- */

ngx_int_t
s3_handle_multipart_abort(ngx_http_request_t *r,
                           const char *fs_path,
                           ngx_http_s3_loc_conf_t *cf,
                           const char *upload_id)
{
    char mpu_dir[PATH_MAX];

    if (!mpu_validate_upload_id(upload_id)) {
        return s3_send_xml_error(r, NGX_HTTP_BAD_REQUEST,
                                 "InvalidArgument",
                                 "The uploadId is invalid.");
    }

    s3_get_mpu_dir(fs_path, upload_id, mpu_dir, sizeof(mpu_dir));

    /*
     * If the staging dir does not exist, AWS S3 returns 404 NoSuchUpload.
     * We do the same: probe with lstat before attempting removal.
     */
    struct stat sb;
    if (lstat(mpu_dir, &sb) != 0) {
        if (errno == ENOENT) {
            return s3_send_xml_error(r, NGX_HTTP_NOT_FOUND,
                                     "NoSuchUpload",
                                     "The specified upload does not exist.");
        }
        XROOTD_S3_METRIC_INC(events_total[XROOTD_S3_EVENT_INTERNAL_ERROR]);
        return NGX_HTTP_INTERNAL_SERVER_ERROR;
    }

    if (mpu_rmdir_recursive(r->connection->log, cf->root_canon, mpu_dir) != 0) {
        XROOTD_S3_METRIC_INC(events_total[XROOTD_S3_EVENT_INTERNAL_ERROR]);
        return NGX_HTTP_INTERNAL_SERVER_ERROR;
    }

    r->headers_out.status           = NGX_HTTP_NO_CONTENT;
    r->headers_out.content_length_n = 0;
    ngx_http_send_header(r);
    return ngx_http_send_special(r, NGX_HTTP_LAST);
}

/* -------------------------------------------------------------------------
 * POST /bucket/key?uploadId=<id>  →  CompleteMultipartUpload (body handler)
 *
 * Called by ngx_http_read_client_request_body() after the XML body listing
 * part numbers has been buffered.  The XML is informational — we concatenate
 * whatever part files exist in ascending order, which is the safe default.
 * ---------------------------------------------------------------------- */

void
s3_multipart_complete_body_handler(ngx_http_request_t *r)
{
    char                   *fs_path;
    ngx_http_s3_loc_conf_t *cf;
    char                    upload_id[25];
    char                    mpu_dir[PATH_MAX];
    char                    final_tmp[PATH_MAX];
    char                    xml_buf[512];
    char                    part_path[PATH_MAX];
    ngx_buf_t              *b;
    ngx_chain_t             out;
    int                     final_fd;
    int                     part_fd;
    int                     part_num;
    ssize_t                 n;
    char                    copy_buf[65536];
    struct stat             st;
    char                    etag[64];
    size_t                  xml_len;
    ngx_uint_t              method_slot;
    u_char                 *p;

    fs_path     = ngx_http_get_module_ctx(r, ngx_http_xrootd_s3_module);
    cf          = ngx_http_get_module_loc_conf(r, ngx_http_xrootd_s3_module);
    method_slot = s3_metrics_method_slot(r);

    if (!s3_get_query_param(r, "uploadId", upload_id, sizeof(upload_id))) {
        s3_metrics_finalize_request_method(r, method_slot,
            s3_send_xml_error(r, NGX_HTTP_BAD_REQUEST,
                              "InvalidArgument", "Missing uploadId."));
        return;
    }

    if (!mpu_validate_upload_id(upload_id)) {
        s3_metrics_finalize_request_method(r, method_slot,
            s3_send_xml_error(r, NGX_HTTP_BAD_REQUEST,
                              "InvalidArgument", "The uploadId is invalid."));
        return;
    }

    s3_get_mpu_dir(fs_path, upload_id, mpu_dir, sizeof(mpu_dir));

    /* Verify the staging directory exists */
    struct stat mpu_sb;
    if (lstat(mpu_dir, &mpu_sb) != 0 || !S_ISDIR(mpu_sb.st_mode)) {
        s3_metrics_finalize_request_method(r, method_slot,
            s3_send_xml_error(r, NGX_HTTP_NOT_FOUND,
                              "NoSuchUpload",
                              "The specified upload does not exist."));
        return;
    }

    /* Build a temp path alongside the final object for atomic rename */
    p = ngx_snprintf((u_char *)final_tmp, sizeof(final_tmp) - 1,
                     "%s.mputmp", fs_path);
    *p = '\0';

    final_fd = xrootd_open_confined_canon(r->connection->log, cf->root_canon,
                                          final_tmp,
                                          O_WRONLY | O_CREAT | O_TRUNC, 0644);
    if (final_fd < 0) {
        ngx_log_error(NGX_LOG_ERR, r->connection->log, errno,
                      "s3 complete_mpu: open(\"%s\") failed", final_tmp);
        XROOTD_S3_METRIC_INC(events_total[XROOTD_S3_EVENT_INTERNAL_ERROR]);
        s3_metrics_finalize_request_method(r, method_slot,
                                           NGX_HTTP_INTERNAL_SERVER_ERROR);
        return;
    }

    /* Concatenate part.1 through part.10000 in order, skipping missing ones */
    for (part_num = 1; part_num <= MPU_MAX_PART_NUMBER; part_num++) {
        p = ngx_snprintf((u_char *)part_path, sizeof(part_path) - 1,
                         "%s/part.%d", mpu_dir, part_num);
        *p = '\0';

        part_fd = xrootd_open_confined_canon(r->connection->log,
                                             cf->root_canon,
                                             part_path, O_RDONLY, 0);
        if (part_fd < 0) {
            if (errno == ENOENT) {
                continue;           /* part not uploaded — skip */
            }
            ngx_log_error(NGX_LOG_ERR, r->connection->log, errno,
                          "s3 complete_mpu: open part %d failed", part_num);
            close(final_fd);
            xrootd_unlink_confined_canon(r->connection->log, cf->root_canon,
                                         final_tmp, 0);
            XROOTD_S3_METRIC_INC(events_total[XROOTD_S3_EVENT_INTERNAL_ERROR]);
            s3_metrics_finalize_request_method(r, method_slot,
                                               NGX_HTTP_INTERNAL_SERVER_ERROR);
            return;
        }

        while ((n = read(part_fd, copy_buf, sizeof(copy_buf))) > 0) {
            if (write(final_fd, copy_buf, (size_t)n) != n) {
                ngx_log_error(NGX_LOG_ERR, r->connection->log, errno,
                              "s3 complete_mpu: write to temp file failed");
                close(part_fd);
                close(final_fd);
                xrootd_unlink_confined_canon(r->connection->log,
                                             cf->root_canon, final_tmp, 0);
                XROOTD_S3_METRIC_INC(
                    events_total[XROOTD_S3_EVENT_INTERNAL_ERROR]);
                s3_metrics_finalize_request_method(
                    r, method_slot, NGX_HTTP_INTERNAL_SERVER_ERROR);
                return;
            }
        }
        close(part_fd);

        if (n < 0) {
            ngx_log_error(NGX_LOG_ERR, r->connection->log, errno,
                          "s3 complete_mpu: read part %d failed", part_num);
            close(final_fd);
            xrootd_unlink_confined_canon(r->connection->log, cf->root_canon,
                                         final_tmp, 0);
            XROOTD_S3_METRIC_INC(events_total[XROOTD_S3_EVENT_INTERNAL_ERROR]);
            s3_metrics_finalize_request_method(r, method_slot,
                                               NGX_HTTP_INTERNAL_SERVER_ERROR);
            return;
        }
    }

    /* Stat the assembled temp file before closing (for ETag) */
    if (fstat(final_fd, &st) != 0) {
        ngx_log_error(NGX_LOG_ERR, r->connection->log, errno,
                      "s3 complete_mpu: fstat temp file failed");
        close(final_fd);
        xrootd_unlink_confined_canon(r->connection->log, cf->root_canon,
                                     final_tmp, 0);
        XROOTD_S3_METRIC_INC(events_total[XROOTD_S3_EVENT_INTERNAL_ERROR]);
        s3_metrics_finalize_request_method(r, method_slot,
                                           NGX_HTTP_INTERNAL_SERVER_ERROR);
        return;
    }
    close(final_fd);

    /* Atomic rename: temp → final object path */
    if (xrootd_rename_confined_canon(r->connection->log, cf->root_canon,
                                     final_tmp, fs_path) != 0)
    {
        ngx_log_error(NGX_LOG_ERR, r->connection->log, errno,
                      "s3 complete_mpu: rename to \"%s\" failed", fs_path);
        xrootd_unlink_confined_canon(r->connection->log, cf->root_canon,
                                     final_tmp, 0);
        XROOTD_S3_METRIC_INC(events_total[XROOTD_S3_EVENT_INTERNAL_ERROR]);
        s3_metrics_finalize_request_method(r, method_slot,
                                           NGX_HTTP_INTERNAL_SERVER_ERROR);
        return;
    }

    /* Clean up staging directory (best-effort — object is already visible) */
    if (mpu_rmdir_recursive(r->connection->log, cf->root_canon, mpu_dir) != 0) {
        ngx_log_error(NGX_LOG_WARN, r->connection->log, 0,
                      "s3 complete_mpu: cleanup of staging dir \"%s\" failed"
                      " (object committed successfully)", mpu_dir);
    }

    /* Build success XML response */
    s3_etag(&st, etag, sizeof(etag));

    xml_len = (size_t)snprintf(xml_buf, sizeof(xml_buf),
        "<?xml version=\"1.0\" encoding=\"UTF-8\"?>\n"
        "<CompleteMultipartUploadResult"
        " xmlns=\"http://s3.amazonaws.com/doc/2006-03-01/\">\n"
        "  <Bucket>%s</Bucket>\n"
        "  <Key>%s</Key>\n"
        "  <ETag>%s</ETag>\n"
        "</CompleteMultipartUploadResult>\n",
        cf->bucket.data ? (const char *)cf->bucket.data : "default",
        /* key is not available here — extract from fs_path */
        strrchr(fs_path, '/') ? strrchr(fs_path, '/') + 1 : fs_path,
        etag);

    if (xml_len >= sizeof(xml_buf)) {
        XROOTD_S3_METRIC_INC(events_total[XROOTD_S3_EVENT_INTERNAL_ERROR]);
        s3_metrics_finalize_request_method(r, method_slot,
                                           NGX_HTTP_INTERNAL_SERVER_ERROR);
        return;
    }

    b = ngx_create_temp_buf(r->pool, xml_len);
    if (b == NULL) {
        XROOTD_S3_METRIC_INC(events_total[XROOTD_S3_EVENT_INTERNAL_ERROR]);
        s3_metrics_finalize_request_method(r, method_slot,
                                           NGX_HTTP_INTERNAL_SERVER_ERROR);
        return;
    }
    ngx_memcpy(b->pos, xml_buf, xml_len);
    b->last    = b->pos + xml_len;
    b->last_buf = 1;

    out.buf  = b;
    out.next = NULL;

    r->headers_out.status           = NGX_HTTP_OK;
    r->headers_out.content_length_n = (off_t)xml_len;
    ngx_http_send_header(r);
    s3_metrics_finalize_request_method(r, method_slot,
                                       ngx_http_output_filter(r, &out));
}

/* -------------------------------------------------------------------------
 * GET /bucket/key?uploadId=<id>  →  ListParts
 * ---------------------------------------------------------------------- */

/*
 * Part entry collected during directory scan.
 */
typedef struct {
    int    part_num;
    off_t  size;
    time_t mtime;
} mpu_part_entry_t;

/*
 * qsort comparator — ascending part number order.
 */
static int
mpu_part_entry_cmp(const void *a, const void *b)
{
    int pa = ((const mpu_part_entry_t *) a)->part_num;
    int pb = ((const mpu_part_entry_t *) b)->part_num;
    return pa - pb;
}

ngx_int_t
s3_handle_list_parts(ngx_http_request_t *r,
                     const char *fs_path,
                     ngx_http_s3_loc_conf_t *cf,
                     const char *key_str)
{
    char              upload_id[128];
    char              mpu_dir[PATH_MAX];
    struct stat       mpu_sb;
    DIR              *dp;
    struct dirent    *de;
    mpu_part_entry_t *parts;
    int               nparts = 0;
    int               i;
    size_t            xml_capacity;
    u_char           *xml;
    size_t            xml_len = 0;
    ngx_buf_t        *b;
    ngx_chain_t       out;
    char              iso_buf[32];
    char              etag_buf[48];

    /* Parse uploadId */
    if (!s3_get_query_param(r, "uploadId", upload_id, sizeof(upload_id))) {
        return s3_send_xml_error(r, NGX_HTTP_BAD_REQUEST,
                                 "InvalidArgument", "Missing uploadId.");
    }

    if (!mpu_validate_upload_id(upload_id)) {
        return s3_send_xml_error(r, NGX_HTTP_BAD_REQUEST,
                                 "InvalidArgument", "The uploadId is invalid.");
    }

    s3_get_mpu_dir(fs_path, upload_id, mpu_dir, sizeof(mpu_dir));

    /* Verify staging directory exists */
    if (lstat(mpu_dir, &mpu_sb) != 0 || !S_ISDIR(mpu_sb.st_mode)) {
        return s3_send_xml_error(r, NGX_HTTP_NOT_FOUND,
                                 "NoSuchUpload",
                                 "The specified upload does not exist.");
    }

    /* Enumerate part.* files — at most MPU_MAX_PART_NUMBER entries */
    parts = ngx_palloc(r->pool,
                       sizeof(mpu_part_entry_t) * (MPU_MAX_PART_NUMBER + 1));
    if (parts == NULL) {
        XROOTD_S3_METRIC_INC(events_total[XROOTD_S3_EVENT_INTERNAL_ERROR]);
        return NGX_HTTP_INTERNAL_SERVER_ERROR;
    }

    dp = opendir(mpu_dir);
    if (dp == NULL) {
        XROOTD_S3_METRIC_INC(events_total[XROOTD_S3_EVENT_INTERNAL_ERROR]);
        return NGX_HTTP_INTERNAL_SERVER_ERROR;
    }

    while ((de = readdir(dp)) != NULL && nparts < MPU_MAX_PART_NUMBER) {
        /* Match "part.<N>" — must start with "part." */
        if (ngx_strncmp(de->d_name, "part.", 5) != 0) {
            continue;
        }

        /* Parse the part number after "part." */
        char *endptr;
        long pn = strtol(de->d_name + 5, &endptr, 10);
        if (*endptr != '\0' || pn < 1 || pn > MPU_MAX_PART_NUMBER) {
            continue;
        }

        /* Stat the part file */
        char part_path[PATH_MAX];
        if ((size_t) snprintf(part_path, sizeof(part_path), "%s/%s",
                              mpu_dir, de->d_name) >= sizeof(part_path)) {
            continue;
        }

        struct stat psb;
        if (lstat(part_path, &psb) != 0 || !S_ISREG(psb.st_mode)) {
            continue;
        }

        parts[nparts].part_num = (int) pn;
        parts[nparts].size     = psb.st_size;
        parts[nparts].mtime    = psb.st_mtime;
        nparts++;
    }
    closedir(dp);

    /* Sort by part number */
    if (nparts > 1) {
        qsort(parts, (size_t) nparts, sizeof(mpu_part_entry_t),
              mpu_part_entry_cmp);
    }

    /* Allocate XML buffer: header + per-part entries */
    xml_capacity = 512
                 + (size_t) cf->bucket.len + 32
                 + strlen(key_str) + 32
                 + strlen(upload_id) + 32
                 + (size_t) nparts * 256;

    xml = ngx_palloc(r->pool, xml_capacity);
    if (xml == NULL) {
        XROOTD_S3_METRIC_INC(events_total[XROOTD_S3_EVENT_INTERNAL_ERROR]);
        return NGX_HTTP_INTERNAL_SERVER_ERROR;
    }

#define XML_APPEND(fmt, ...) \
    xml_len += (size_t) snprintf((char *) xml + xml_len, \
                                 xml_capacity - xml_len, fmt, ##__VA_ARGS__)

    XML_APPEND("<?xml version=\"1.0\" encoding=\"UTF-8\"?>\n");
    XML_APPEND("<ListPartsResult"
               " xmlns=\"http://s3.amazonaws.com/doc/2006-03-01/\">\n");
    XML_APPEND("  <Bucket>%.*s</Bucket>\n",
               (int) cf->bucket.len,
               cf->bucket.data ? (const char *) cf->bucket.data : "");
    XML_APPEND("  <Key>%s</Key>\n", key_str);
    XML_APPEND("  <UploadId>%s</UploadId>\n", upload_id);
    XML_APPEND("  <StorageClass>STANDARD</StorageClass>\n");
    XML_APPEND("  <IsTruncated>false</IsTruncated>\n");

    for (i = 0; i < nparts; i++) {
        s3_iso8601(parts[i].mtime, iso_buf, sizeof(iso_buf));
        snprintf(etag_buf, sizeof(etag_buf),
                 "\"%ld-%lld\"",
                 (long) parts[i].mtime,
                 (long long) parts[i].size);
        XML_APPEND("  <Part>\n");
        XML_APPEND("    <PartNumber>%d</PartNumber>\n", parts[i].part_num);
        XML_APPEND("    <LastModified>%s</LastModified>\n", iso_buf);
        XML_APPEND("    <ETag>%s</ETag>\n", etag_buf);
        XML_APPEND("    <Size>%lld</Size>\n", (long long) parts[i].size);
        XML_APPEND("  </Part>\n");
    }

    XML_APPEND("</ListPartsResult>\n");

#undef XML_APPEND

    b = ngx_create_temp_buf(r->pool, xml_len);
    if (b == NULL) {
        XROOTD_S3_METRIC_INC(events_total[XROOTD_S3_EVENT_INTERNAL_ERROR]);
        return NGX_HTTP_INTERNAL_SERVER_ERROR;
    }
    ngx_memcpy(b->pos, xml, xml_len);
    b->last     = b->pos + xml_len;
    b->last_buf = 1;

    out.buf  = b;
    out.next = NULL;

    r->headers_out.status            = NGX_HTTP_OK;
    r->headers_out.content_type      = (ngx_str_t) ngx_string("application/xml");
    r->headers_out.content_length_n  = (off_t) xml_len;
    ngx_http_send_header(r);
    return ngx_http_output_filter(r, &out);
}

/* -------------------------------------------------------------------------
 * GET /bucket/?uploads  →  ListMultipartUploads
 * ---------------------------------------------------------------------- */

/*
 * One in-progress upload record collected during the bucket scan.
 *
 * Key is bounded by NAME_MAX (255 on Linux): the staging directory name is
 * ".<key>.mpu-<upload_id>" and must fit in a single directory entry.
 * We use NAME_MAX + 1 for safety.
 */
typedef struct {
    char   key[NAME_MAX + 1];  /* object key (without leading '.') */
    char   upload_id[128];     /* upload id token                  */
    time_t mtime;              /* staging dir mtime (≈ initiation) */
} mpu_upload_entry_t;

/*
 * Maximum number of concurrent in-progress uploads we enumerate in a single
 * ListMultipartUploads response.  AWS S3 returns at most 1000 per page; we
 * match that default since truncation/pagination is not implemented here.
 */
#define MPU_MAX_LISTED_UPLOADS  1000

ngx_int_t
s3_handle_list_multipart_uploads(ngx_http_request_t *r,
                                  ngx_http_s3_loc_conf_t *cf)
{
    DIR                *dp;
    struct dirent      *de;
    mpu_upload_entry_t *uploads;
    int                 nuploads = 0;
    int                 i;
    size_t              xml_capacity;
    u_char             *xml;
    size_t              xml_len = 0;
    ngx_buf_t          *b;
    ngx_chain_t         out;
    char                iso_buf[32];

    /*
     * root.data is the configured filesystem root — that is the bucket
     * directory.  Hidden directories inside it of the form:
     *   .<key>.mpu-<upload_id>
     * represent in-progress multipart uploads.
     */
    if (cf->root.data == NULL || cf->root.len == 0) {
        XROOTD_S3_METRIC_INC(events_total[XROOTD_S3_EVENT_INTERNAL_ERROR]);
        return NGX_HTTP_INTERNAL_SERVER_ERROR;
    }

    /* Collect matching entries first so we can size the XML buffer accurately. */
    uploads = ngx_palloc(r->pool,
                         sizeof(mpu_upload_entry_t) * MPU_MAX_LISTED_UPLOADS);
    if (uploads == NULL) {
        XROOTD_S3_METRIC_INC(events_total[XROOTD_S3_EVENT_INTERNAL_ERROR]);
        return NGX_HTTP_INTERNAL_SERVER_ERROR;
    }

    dp = opendir((const char *) cf->root.data);
    if (dp == NULL) {
        XROOTD_S3_METRIC_INC(events_total[XROOTD_S3_EVENT_INTERNAL_ERROR]);
        return NGX_HTTP_INTERNAL_SERVER_ERROR;
    }

    while ((de = readdir(dp)) != NULL && nuploads < MPU_MAX_LISTED_UPLOADS) {
        /*
         * We are looking for hidden directories (name starts with '.') that
         * contain ".mpu-" — pattern: .<key>.mpu-<upload_id>
         */
        if (de->d_name[0] != '.') {
            continue;
        }
        if (de->d_name[1] == '\0'
            || (de->d_name[1] == '.' && de->d_name[2] == '\0'))
        {
            continue;   /* "." or ".." */
        }

        /* Find ".mpu-" marker — must appear after the key portion */
        const char *mpu_marker = strstr(de->d_name + 1, ".mpu-");
        if (mpu_marker == NULL) {
            continue;
        }

        /* Verify it is actually a directory */
        char child_path[PATH_MAX];
        if ((size_t) snprintf(child_path, sizeof(child_path), "%s/%s",
                              (const char *) cf->root.data,
                              de->d_name) >= sizeof(child_path)) {
            continue;
        }
        struct stat sb;
        if (lstat(child_path, &sb) != 0 || !S_ISDIR(sb.st_mode)) {
            continue;
        }

        /*
         * Name format: ".<key>.mpu-<upload_id>"
         * key  = de->d_name[1 .. mpu_marker-1]
         * upload_id = mpu_marker + 5   (skip ".mpu-")
         */
        size_t key_len = (size_t) (mpu_marker - (de->d_name + 1));
        const char *uid = mpu_marker + 5;

        if (key_len == 0 || uid[0] == '\0') {
            continue;
        }
        if (key_len >= sizeof(uploads[nuploads].key)
            || strlen(uid) >= sizeof(uploads[nuploads].upload_id)) {
            continue;
        }

        ngx_memcpy(uploads[nuploads].key, de->d_name + 1, key_len);
        uploads[nuploads].key[key_len] = '\0';
        ngx_cpystrn((u_char *) uploads[nuploads].upload_id,
                    (u_char *) uid,
                    sizeof(uploads[nuploads].upload_id));
        uploads[nuploads].mtime = sb.st_mtime;
        nuploads++;
    }

    closedir(dp);

    /* Size the XML buffer: header + per-upload block */
    xml_capacity = 512
                 + (size_t) cf->bucket.len + 32
                 + (size_t) nuploads * (NAME_MAX + 256);

    xml = ngx_palloc(r->pool, xml_capacity);
    if (xml == NULL) {
        XROOTD_S3_METRIC_INC(events_total[XROOTD_S3_EVENT_INTERNAL_ERROR]);
        return NGX_HTTP_INTERNAL_SERVER_ERROR;
    }

#define XML_APPEND(fmt, ...) \
    xml_len += (size_t) snprintf((char *) xml + xml_len, \
                                 xml_capacity - xml_len, fmt, ##__VA_ARGS__)

    XML_APPEND("<?xml version=\"1.0\" encoding=\"UTF-8\"?>\n");
    XML_APPEND("<ListMultipartUploadsResult"
               " xmlns=\"http://s3.amazonaws.com/doc/2006-03-01/\">\n");
    XML_APPEND("  <Bucket>%.*s</Bucket>\n",
               (int) cf->bucket.len,
               cf->bucket.data ? (const char *) cf->bucket.data : "");
    XML_APPEND("  <IsTruncated>false</IsTruncated>\n");

    for (i = 0; i < nuploads; i++) {
        s3_iso8601(uploads[i].mtime, iso_buf, sizeof(iso_buf));
        XML_APPEND("  <Upload>\n");
        XML_APPEND("    <Key>%s</Key>\n", uploads[i].key);
        XML_APPEND("    <UploadId>%s</UploadId>\n", uploads[i].upload_id);
        XML_APPEND("    <StorageClass>STANDARD</StorageClass>\n");
        XML_APPEND("    <Initiated>%s</Initiated>\n", iso_buf);
        XML_APPEND("  </Upload>\n");
    }

    XML_APPEND("</ListMultipartUploadsResult>\n");

#undef XML_APPEND

    b = ngx_create_temp_buf(r->pool, xml_len);
    if (b == NULL) {
        XROOTD_S3_METRIC_INC(events_total[XROOTD_S3_EVENT_INTERNAL_ERROR]);
        return NGX_HTTP_INTERNAL_SERVER_ERROR;
    }
    ngx_memcpy(b->pos, xml, xml_len);
    b->last     = b->pos + xml_len;
    b->last_buf = 1;

    out.buf  = b;
    out.next = NULL;

    r->headers_out.status            = NGX_HTTP_OK;
    r->headers_out.content_type      = (ngx_str_t) ngx_string("application/xml");
    r->headers_out.content_length_n  = (off_t) xml_len;
    ngx_http_send_header(r);
    return ngx_http_output_filter(r, &out);
}

/* -------------------------------------------------------------------------
 * PUT /bucket/key?partNumber=N&uploadId=<id>  +  x-amz-copy-source header
 * →  UploadPartCopy
 * ---------------------------------------------------------------------- */

static const char *
s3_find_request_header(ngx_http_request_t *r, const char *name, size_t name_len)
{
    ngx_list_part_t *part;
    ngx_table_elt_t *h;
    ngx_uint_t       i;

    part = &r->headers_in.headers.part;
    h    = part->elts;
    for (i = 0; ; i++) {
        if (i >= part->nelts) {
            if (part->next == NULL) {
                break;
            }
            part = part->next;
            h    = part->elts;
            i    = 0;
        }
        if (h[i].key.len == name_len
            && ngx_strncasecmp(h[i].key.data, (u_char *) name, name_len) == 0)
        {
            return (const char *) h[i].value.data;
        }
    }
    return NULL;
}

ngx_int_t
s3_handle_upload_part_copy(ngx_http_request_t *r,
                            const char *fs_path,
                            ngx_http_s3_loc_conf_t *cf,
                            const char *upload_id,
                            int part_num)
{
    const char  *copy_src_hdr;
    const char  *src_key;
    char         src_fs_path[PATH_MAX];
    char         mpu_dir[PATH_MAX];
    char         part_path[PATH_MAX];
    char         xml_buf[512];
    char         etag_buf[64];
    char         iso_buf[32];
    struct stat  src_sb, part_sb;
    int          src_fd, dst_fd;
    ssize_t      nr;
    char         iobuf[65536];
    u_char      *p;
    ngx_buf_t   *b;
    ngx_chain_t  out;
    size_t       xml_len;

    copy_src_hdr = s3_find_request_header(r, "x-amz-copy-source",
                                          sizeof("x-amz-copy-source") - 1);
    if (copy_src_hdr == NULL) {
        return s3_send_xml_error(r, NGX_HTTP_BAD_REQUEST,
                                 "InvalidArgument",
                                 "Missing x-amz-copy-source header.");
    }

    /* Strip leading '/' then skip the bucket prefix component */
    src_key = copy_src_hdr;
    if (*src_key == '/') {
        src_key++;
    }
    {
        const char *slash = strchr(src_key, '/');
        if (slash != NULL) {
            src_key = slash + 1;
        }
    }

    /* Build and validate source path */
    if ((size_t) snprintf(src_fs_path, sizeof(src_fs_path), "%s/%s",
                          cf->root_canon, src_key) >= sizeof(src_fs_path)
        || strstr(src_fs_path, "/../") != NULL
        || strncmp(src_fs_path, cf->root_canon,
                   strlen(cf->root_canon)) != 0)
    {
        return s3_send_xml_error(r, NGX_HTTP_BAD_REQUEST,
                                 "InvalidArgument",
                                 "Invalid copy source path.");
    }

    if (stat(src_fs_path, &src_sb) != 0 || !S_ISREG(src_sb.st_mode)) {
        return s3_send_xml_error(r, NGX_HTTP_NOT_FOUND,
                                 "NoSuchKey",
                                 "The specified copy source does not exist.");
    }

    if (!mpu_validate_upload_id(upload_id)) {
        return s3_send_xml_error(r, NGX_HTTP_BAD_REQUEST,
                                 "InvalidArgument",
                                 "The uploadId is invalid.");
    }

    s3_get_mpu_dir(fs_path, upload_id, mpu_dir, sizeof(mpu_dir));
    if (stat(mpu_dir, &part_sb) != 0) {
        return s3_send_xml_error(r, NGX_HTTP_NOT_FOUND,
                                 "NoSuchUpload",
                                 "The specified upload does not exist.");
    }

    p = ngx_snprintf((u_char *) part_path, sizeof(part_path) - 1,
                     "%s/part.%z", mpu_dir, (size_t) part_num);
    *p = '\0';

    /* Copy source file → part file */
    src_fd = open(src_fs_path, O_RDONLY);
    if (src_fd < 0) {
        XROOTD_S3_METRIC_INC(events_total[XROOTD_S3_EVENT_INTERNAL_ERROR]);
        return NGX_HTTP_INTERNAL_SERVER_ERROR;
    }

    dst_fd = open(part_path, O_WRONLY | O_CREAT | O_TRUNC, 0600);
    if (dst_fd < 0) {
        close(src_fd);
        XROOTD_S3_METRIC_INC(events_total[XROOTD_S3_EVENT_INTERNAL_ERROR]);
        return NGX_HTTP_INTERNAL_SERVER_ERROR;
    }

    while ((nr = read(src_fd, iobuf, sizeof(iobuf))) > 0) {
        char   *wbuf      = iobuf;
        ssize_t remaining = nr;
        while (remaining > 0) {
            ssize_t nw = write(dst_fd, wbuf, (size_t) remaining);
            if (nw <= 0) {
                if (errno == EINTR) {
                    continue;
                }
                close(src_fd);
                close(dst_fd);
                unlink(part_path);
                XROOTD_S3_METRIC_INC(
                    events_total[XROOTD_S3_EVENT_INTERNAL_ERROR]);
                return NGX_HTTP_INTERNAL_SERVER_ERROR;
            }
            wbuf      += nw;
            remaining -= nw;
        }
    }
    close(src_fd);
    close(dst_fd);

    if (stat(part_path, &part_sb) != 0) {
        XROOTD_S3_METRIC_INC(events_total[XROOTD_S3_EVENT_INTERNAL_ERROR]);
        return NGX_HTTP_INTERNAL_SERVER_ERROR;
    }

    s3_etag(&part_sb, etag_buf, sizeof(etag_buf));
    s3_iso8601(part_sb.st_mtime, iso_buf, sizeof(iso_buf));

    xml_len = (size_t) snprintf(xml_buf, sizeof(xml_buf),
        "<?xml version=\"1.0\" encoding=\"UTF-8\"?>"
        "<CopyPartResult "
        "xmlns=\"http://s3.amazonaws.com/doc/2006-03-01/\">"
        "<ETag>%s</ETag>"
        "<LastModified>%s</LastModified>"
        "</CopyPartResult>",
        etag_buf, iso_buf);

    b = ngx_create_temp_buf(r->pool, xml_len);
    if (b == NULL) {
        XROOTD_S3_METRIC_INC(events_total[XROOTD_S3_EVENT_INTERNAL_ERROR]);
        return NGX_HTTP_INTERNAL_SERVER_ERROR;
    }
    ngx_memcpy(b->pos, xml_buf, xml_len);
    b->last     = b->pos + xml_len;
    b->last_buf = 1;

    out.buf  = b;
    out.next = NULL;

    r->headers_out.status            = NGX_HTTP_OK;
    r->headers_out.content_type      = (ngx_str_t) ngx_string("application/xml");
    r->headers_out.content_length_n  = (off_t) xml_len;
    ngx_http_send_header(r);
    return ngx_http_output_filter(r, &out);
}
