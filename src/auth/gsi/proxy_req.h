#ifndef XROOTD_GSI_PROXY_REQ_H
#define XROOTD_GSI_PROXY_REQ_H

/* ---- GSI X.509 proxy delegation crypto (phase-57 §F6) ----
 *
 * RFC-3820 proxy-certificate request/sign/assemble primitives, ngx-free
 * (OpenSSL only) so they can be unit-tested standalone and shared by the inbound
 * capture (src/gsi/auth.c) and the outbound use (src/tpc). Ported faithfully from
 * stock XrdSecgsi (XrdCrypto/XrdCryptosslgsiAux.cc: X509CreateProxyReq /
 * X509SignProxyReq), validated against a stock -dlgpxy:request source — see
 * tests/test_tpc_delegation.py. */

#include <stddef.h>
#include <stdint.h>
#include <openssl/evp.h>

/*
 * xrootd_gsi_build_pxyreq — build the server→client kXGS_pxyreq payload: an
 * RFC-3820 proxy-certificate REQUEST for `parent_pem` (the peer's EEC/proxy, PEM).
 *
 * Generates a fresh RSA key (>= parent bits, min 2048, exp 65537), names the
 * subject `<parent subject>/CN=<random serial>`, attaches a CRITICAL
 * proxyCertInfo extension (impersonation policy, pathlen derived from the parent),
 * copies the parent's other extensions (minus subjectAltName / proxyCertInfo), and
 * self-signs the request with SHA-256.
 *
 * On success returns 0 with *newkey = the fresh keypair (caller EVP_PKEY_free) and
 * *req_der / *req_len = a malloc'd DER X509_REQ (caller free()). On failure returns
 * -1 and, if err != NULL, writes a short reason into err[errcap]; no output is set.
 */
int xrootd_gsi_build_pxyreq(const uint8_t *parent_pem, size_t parent_pem_len,
                            EVP_PKEY **newkey, uint8_t **req_der, size_t *req_len,
                            char *err, size_t errcap);

/*
 * xrootd_gsi_sign_pxyreq — ISSUE a proxy by signing a request (the delegating
 * client's role, kXGC_sigpxy). `signer_pem` is the issuer EEC/proxy (PEM, leaf
 * first), `signer_key` its private key, `req_der`/`req_len` the request from
 * xrootd_gsi_build_pxyreq.
 *
 * Validates the request subject is `<signer subject>/CN=<serial>`, builds an
 * X.509 v3 proxy (serial = the request CN, subject = request subject, issuer =
 * signer subject, pubkey = request pubkey, validity = the signer's remaining
 * lifetime), copies the signer's non-SAN/non-pci extensions (keyUsage required;
 * a subjectAltName is rejected), adds a CRITICAL proxyCertInfo with the correctly
 * decremented path-length, and signs with `signer_key` (SHA-256).
 *
 * On success returns 0 with proxy_pem + proxy_len = the malloc'd signed proxy
 * certificate (PEM, caller free()). -1 with err[errcap] on failure.
 */
int xrootd_gsi_sign_pxyreq(const uint8_t *signer_pem, size_t signer_pem_len,
                           EVP_PKEY *signer_key,
                           const uint8_t *req_der, size_t req_len,
                           uint8_t **proxy_pem, size_t *proxy_len,
                           char *err, size_t errcap);

/*
 * xrootd_gsi_assemble_proxy — ASSEMBLE the delegated proxy credential (the GSI
 * server / TPC destination's role after kXGC_sigpxy). Pairs the client-signed
 * proxy cert (`proxy_pem`) with the saved request key (`reqkey`, the private half
 * generated by build_pxyreq) and prepends it to the signer's chain (`chain_pem`,
 * the client's EEC chain) to produce a usable proxy chain PEM (proxy first).
 *
 * Verifies the proxy public key matches `reqkey`. On success returns 0 with
 * out_pem + out_len = the malloc'd `<proxy><chain>` PEM (caller free()) — feed it
 * (with `reqkey`) to xrootd_gsi_build_cert_response to authenticate as the user.
 * -1 with err[errcap] on failure (e.g. key mismatch).
 */
int xrootd_gsi_assemble_proxy(const uint8_t *proxy_pem, size_t proxy_pem_len,
                              EVP_PKEY *reqkey,
                              const uint8_t *chain_pem, size_t chain_pem_len,
                              uint8_t **out_pem, size_t *out_len,
                              char *err, size_t errcap);

#endif /* XROOTD_GSI_PROXY_REQ_H */
