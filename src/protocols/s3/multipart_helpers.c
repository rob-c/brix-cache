/*
 * s3_has_query_flag — return 1 if <key> appears as a bare flag (no '=') in
 * the request query string, 0 otherwise.
 */
int
s3_has_query_flag(ngx_http_request_t *r, const char *key)
{
    return brix_http_query_has(r->args, key, 0);
}

/*
 * s3_get_query_param — copy the value of query parameter <key> into <out>.
 * Returns 1 on success, 0 if the key is absent or the value is empty.
 */
int
s3_get_query_param(ngx_http_request_t *r, const char *key,
                   char *out, size_t outsz)
{
    return brix_http_query_get(r->args, key, out, outsz,
                                 BRIX_HTTP_QUERY_TRUNCATE) > 0;
}

/*
 * MPU path helpers
 * */

/*
 * s3_get_mpu_dir — build the staging directory path for an in-progress MPU.
 *
 * Given fs_path = "/export/bucket/subdir/key.root" and upload_id "abc123",
 * produces "/export/bucket/subdir/.key.root.mpu-abc123".
 *
 * The leading '.' keeps the directory hidden from S3 LIST responses and
 * ordinary directory listings.
 */
void
s3_get_mpu_dir(const char *fs_path, const char *upload_id,
               char *out_dir, size_t outsz)
{
    const char *last_slash = strrchr(fs_path, '/');
    u_char     *p;

    if (last_slash != NULL) {
        size_t dir_prefix_len = (size_t)(last_slash - fs_path) + 1; /* includes '/' */
        p = ngx_snprintf((u_char *)out_dir, outsz - 1,
                         "%*s.%s.mpu-%s",
                         (int)dir_prefix_len, fs_path,
                         last_slash + 1, upload_id);
    } else {
        p = ngx_snprintf((u_char *)out_dir, outsz - 1,
                         ".%s.mpu-%s", fs_path, upload_id);
    }
    *p = '\0';
}

/*
 * mpu_validate_upload_id — return 1 if upload_id contains only safe hex
 * characters (as generated by s3_handle_multipart_initiate).  Rejects any
 * value that could affect path construction.
 */
int
mpu_validate_upload_id(const char *upload_id)
{
    const char *p = upload_id;

    if (*p == '\0') {
        return 0;
    }
    for (; *p != '\0'; p++) {
        if (!((*p >= '0' && *p <= '9') || (*p >= 'a' && *p <= 'f'))) {
            return 0;
        }
    }
    return 1;
}

/*
 * Safe recursive directory removal — no shell subprocess
 * */

/*
 * mpu_rmdir_recursive — remove <path> and all its contents using
 * opendir/unlinkat.  Path confinement is enforced via
 * brix_unlink_confined_canon for every remove call.
 *
 * Returns 0 on success, -1 on error (errno set).
 */
int
mpu_rmdir_recursive(ngx_log_t *log, const char *root_canon, const char *path)
{
    return brix_fs_remove_tree_confined(log, root_canon, path) == NGX_OK
           ? 0
           : -1;
}

/*
 * Phase 39 (WS8/HTTP-2): reap abandoned multipart staging directories.
 *
 * WHAT: scan the single directory holding final_path for staging dirs named
 *       ".<obj>.mpu-<uploadid>" (the layout s3_get_mpu_dir produces) whose mtime
 *       is older than max_age_secs, and remove each via mpu_rmdir_recursive.
 *       Returns the number reaped; no-op (0) when max_age_secs <= 0.
 *
 * WHY:  a client that uploads parts and then never sends Complete or Abort leaks
 *       its staging dir forever — unbounded disk growth → ENOSPC DoS.  Reaping on
 *       MPU initiate keeps active directories self-cleaning, mirroring AWS S3's
 *       incomplete-multipart lifecycle expiry.
 *
 * HOW:  one bounded readdir of final_path's parent directory (NOT recursive, not
 *       a whole-tree walk).  An active upload keeps its staging-dir mtime fresh
 *       (each new part.<N> bumps it), so only genuinely idle uploads are reaped;
 *       mpu_rmdir_recursive enforces root_canon confinement on every removal.
 */
int
s3_mpu_reap_stale(ngx_http_request_t *r, ngx_http_s3_loc_conf_t *cf,
                  const char *final_path, time_t max_age_secs)
{
    ngx_log_t        *log = r->connection->log;
    const char       *root_canon = cf->common.root_canon;
    char              dir[PATH_MAX];
    const char       *slash;
    brix_vfs_ctx_t  vctx;
    brix_vfs_dir_t *d;
    time_t            now;
    size_t            dlen;
    int               reaped = 0;

    if (max_age_secs <= 0 || final_path == NULL) {
        return 0;
    }

    slash = strrchr(final_path, '/');
    if (slash == NULL || slash == final_path) {
        return 0;   /* no usable parent directory */
    }
    dlen = (size_t) (slash - final_path);
    if (dlen >= sizeof(dir)) {
        return 0;
    }
    ngx_memcpy(dir, final_path, dlen);
    dir[dlen] = '\0';

    /* Scan the staging dir AS THE MAPPED USER under impersonation (0700, owned by
     * that user) — a bare worker opendir would EACCES. Non-metered (this reaper
     * is opportunistic maintenance, not a user LIST). */
    s3_build_vfs_ctx(r, dir, cf, &vctx);
    d = brix_vfs_opendir_quiet(&vctx, NULL);
    if (d == NULL) {
        return 0;
    }

    now = time(NULL);

    for ( ;; ) {
        ngx_str_t          name;
        const char        *dname;
        char               full[PATH_MAX];
        brix_vfs_ctx_t   cctx;
        brix_vfs_stat_t  cst;

        if (brix_vfs_readdir_kind(d, &name, NULL) != NGX_OK) {
            break;   /* end of stream or error */
        }
        dname = (const char *) name.data;

        /* Match the staging-dir naming ".<objname>.mpu-<uploadid>" only. */
        if (dname[0] != '.' || strstr(dname, ".mpu-") == NULL) {
            continue;
        }
        if (snprintf(full, sizeof(full), "%s/%s", dir, dname)
            >= (int) sizeof(full)) {
            continue;
        }
        /* Confined no-follow probe for the dir check + mtime (non-metered). */
        s3_build_vfs_ctx(r, full, cf, &cctx);
        if (brix_vfs_probe(&cctx, 1 /* no-follow */, &cst) != NGX_OK
            || !cst.is_directory)
        {
            continue;
        }
        if ((now - cst.mtime) <= max_age_secs) {
            continue;   /* recently active — keep */
        }
        if (mpu_rmdir_recursive(log, root_canon, full) == 0) {
            reaped++;
        }
    }

    brix_vfs_closedir(d, log);

    if (reaped > 0 && log != NULL) {
        ngx_log_error(NGX_LOG_WARN, log, 0,
                      "s3: reaped %d abandoned multipart staging dir(s) "
                      "(idle >%T s) under \"%s\"",
                      reaped, max_age_secs, dir);
    }
    return reaped;
}
