/*
 * key_registry.c — shared-memory TPC key registry.
 *
 * Manages the in-flight TPC key table used for native root:// TPC rendezvous.
 * The registry is a fixed-size array in a shared memory zone protected by a
 * spinlock so that all nginx worker processes share the same key namespace.
 */

#include "key_registry.h"
#include "compat/shm_slots.h"

#include <string.h>
#include <openssl/crypto.h>   /* CRYPTO_memcmp — constant-time key compare */

/*
 * tpc_key_eq — constant-time equality of a presented TPC key against a stored
 * registry entry. The TPC key is a transfer rendezvous secret, so a
 * timing-variable strcmp would be a byte-by-byte guessing oracle. Both sides are
 * compared over the full fixed-width field (entries are NUL-padded), so timing
 * does not depend on the matching prefix length; an over-long presented key is
 * rejected up front (it can never be a valid stored key).
 */
static int
tpc_key_eq(const char *stored, const char *presented)
{
    char     buf[XROOTD_TPC_KEY_LEN];
    size_t   n = ngx_strlen(presented);

    if (n >= XROOTD_TPC_KEY_LEN) {
        return 0;
    }
    ngx_memzero(buf, sizeof(buf));
    ngx_memcpy(buf, presented, n);
    return CRYPTO_memcmp(stored, buf, XROOTD_TPC_KEY_LEN) == 0;
}

static ngx_shm_zone_t  *xrootd_tpc_key_shm_zone;
static ngx_shmtx_t      xrootd_tpc_key_mutex;
static ngx_uint_t        tpc_key_seq;

/*
 *
 * WHAT: Returns a pointer to the TPC key table stored in shared memory. This
 *       static helper abstracts access to the shared-memory zone so other
 *       functions don't need to repeat the NULL-check logic for accessing the
 *       registry data structure.
 *
 * WHY: Provides single point of failure handling — if the shm_zone hasn't been
 *       initialized yet (data == (void*)1 is a marker meaning "pending init"),
 *       all callers return NULL safely without dereferencing invalid pointers. */
static xrootd_tpc_key_table_t *
key_table(void)
{
    if (xrootd_tpc_key_shm_zone == NULL
        || xrootd_tpc_key_shm_zone->data == NULL
        || xrootd_tpc_key_shm_zone->data == (void *) 1)
    {
        return NULL;
    }
    return (xrootd_tpc_key_table_t *) xrootd_tpc_key_shm_zone->data;
}

/*
 *
 * WHAT: Initializes the shared-memory zone that holds the TPC key registry. Called
 *       during nginx configuration phase when ngx_shared_memory_add() creates the
 *       shm_zone — this init callback allocates and zeroes the key table structure,
 *       then creates the spinlock mutex for cross-worker synchronization.
 *
 * WHY: The key registry must be shared across all nginx worker processes so that
 *       TPC rendezvous keys generated by one worker (during a pull transfer) can
 *       be validated by another worker (when a destination server connects to fetch
 *       the file). Shared memory + spinlock ensures atomic access without race conditions. */
static ngx_int_t
xrootd_tpc_key_shm_init_zone(ngx_shm_zone_t *shm_zone, void *data)
{
    xrootd_tpc_key_table_t *tbl;
    ngx_flag_t              fresh;

    /*
     * Allocate the table FROM the slab pool so the ngx_slab_pool_t header at
     * shm.addr survives. nginx's ngx_unlock_mutexes() (run on every child
     * death) force-unlocks that header's mutex; laying our own struct over
     * shm.addr would clobber it and SIGSEGV the master when any child exits.
     * The helper also creates xrootd_tpc_key_mutex from the table's first
     * member (the ngx_shmtx_sh_t lock) and zeroes the table on fresh alloc.
     */
    tbl = xrootd_shm_table_alloc(shm_zone, data,
                                 sizeof(xrootd_tpc_key_table_t),
                                 &xrootd_tpc_key_mutex, &fresh);
    if (tbl == NULL) {
        return NGX_ERROR;
    }

    /*
     * The table is a fixed array of slots that begins entirely zeroed (in_use
     * == 0 everywhere), which the helper guarantees on fresh allocation. There
     * are no live counters to seed, so nothing extra is required here; on reuse
     * we must NOT touch the table to preserve in-flight key state.
     */
    if (fresh) {
        /* No additional fresh-only field initialisation required. */
    }

    return NGX_OK;
}

/*
 *
 * WHAT: Configures the shared-memory zone that holds the TPC key registry during
 *       nginx configuration phase. Allocates a fixed-size memory region ("xrootd_tpc_keys")
 *       large enough to hold the key table structure plus one page of padding, then
 *       registers the init callback and sets data=(void*)1 as a "pending initialization"
 *       marker so that no worker can access the registry before it's ready.
 *
 * WHY: Native TPC transfers require all nginx workers to share the same key namespace.
 *       A pull transfer (worker fetching file from source) generates a key; when the
 *       destination server connects, its worker must validate that same key. Shared memory
 *       is the only mechanism that allows cross-process atomic access without IPC overhead. */
ngx_int_t
xrootd_tpc_key_configure_registry(ngx_conf_t *cf)
{
    ngx_str_t  zone_name = ngx_string("xrootd_tpc_keys");
    size_t     zone_size;

    zone_size = xrootd_shm_zone_size(sizeof(xrootd_tpc_key_table_t));
    xrootd_tpc_key_shm_zone = ngx_shared_memory_add(cf, &zone_name,
                                                      zone_size,
                                                      &ngx_stream_xrootd_module);
    if (xrootd_tpc_key_shm_zone == NULL) {
        return NGX_ERROR;
    }

    xrootd_tpc_key_shm_zone->init = xrootd_tpc_key_shm_init_zone;
    xrootd_tpc_key_shm_zone->data = (void *) 1;

    return NGX_OK;
}

/*
 *
 * WHAT: Generates a unique TPC transfer key string in the format "PID+SEQ" using
 *       snprintf. Combines the nginx worker's process ID with a per-process incrementing
 *       sequence counter to produce an 8-character PID + 16-character SEQ = 24-byte hex key.
 *
 * WHY: Each TPC transfer needs a unique rendezvous identifier so that multiple concurrent
 *       transfers don't collide in the shared registry. The pid×seq approach avoids needing
 *       atomics (every nginx function runs single-threaded in the event loop) while still
 *       guaranteeing uniqueness across all workers and all transfers. */
void
xrootd_tpc_generate_key(char *buf, size_t buf_sz)
{
    /* Unique per process (pid) × per call (seq).  No atomics needed —
     * this is always called from the single-threaded event loop. */
    tpc_key_seq++;
    (void) snprintf(buf, buf_sz, "%08lx%016lx",
                    (unsigned long) ngx_pid,
                    (unsigned long) tpc_key_seq);
}

/*
 *
 * WHAT: Registers a TPC key in the shared-memory registry with a time-to-live (TTL). The
 *       function scans all slots looking for either an expired slot to reuse, or an existing
 *       duplicate key entry that needs expiry refresh. If no slot is available, the key is
 *       silently dropped — the caller should retry or use a different transfer strategy.
 *
 * WHY: TPC transfers need temporary rendezvous identifiers that expire after the transfer
 *       completes (or fails). The TTL mechanism prevents stale keys from accumulating in the
 *       registry indefinitely while allowing concurrent transfers to coexist without collisions. */
void
xrootd_tpc_key_register(const char *key, ngx_msec_t ttl_ms)
{
    xrootd_tpc_key_table_t *tbl;
    xrootd_tpc_key_entry_t *e;
    ngx_uint_t               i, free_slot;
    ngx_msec_t               now;

    tbl = key_table();
    if (tbl == NULL || key == NULL || key[0] == '\0') {
        return;
    }

    now = ngx_current_msec;

    ngx_shmtx_lock(&xrootd_tpc_key_mutex);

    free_slot = XROOTD_TPC_KEY_SLOTS;
    for (i = 0; i < XROOTD_TPC_KEY_SLOTS; i++) {
        e = &tbl->slots[i];
        if (!e->in_use || xrootd_shm_slot_expired(now, e->expiry)) {
            xrootd_shm_remember_free_slot(&free_slot, XROOTD_TPC_KEY_SLOTS,
                                          i);
            if (e->in_use && xrootd_shm_slot_expired(now, e->expiry)) {
                ngx_memzero(e, sizeof(*e));
            }
            continue;
        }
        if (tpc_key_eq(e->key, key)) {
            /* refresh expiry */
            e->expiry = now + ttl_ms;
            ngx_shmtx_unlock(&xrootd_tpc_key_mutex);
            return;
        }
    }

    if (free_slot < XROOTD_TPC_KEY_SLOTS) {
        e = &tbl->slots[free_slot];
        ngx_memzero(e, sizeof(*e));
        ngx_cpystrn((u_char *) e->key, (u_char *) key, sizeof(e->key));
        e->expiry = now + ttl_ms;
        e->in_use = 1;
    }

    ngx_shmtx_unlock(&xrootd_tpc_key_mutex);
}

/*
 *
 * WHAT: Checks whether a TPC key exists in the registry and has not yet expired. Returns 1 (true)
 *       if the key is found and active, 0 (false) if not found or already expired. Expired entries
 *       are silently zeroed during validation to keep the registry clean.
 *
 * WHY: The destination server in a TPC transfer must validate that its rendezvous key is still valid
 *       before attempting to connect to fetch the file. This prevents connections to stale transfers
 *       or transfers whose source worker has already finished and cleaned up. */
int
xrootd_tpc_key_validate(const char *key)
{
    xrootd_tpc_key_table_t *tbl;
    xrootd_tpc_key_entry_t *e;
    ngx_uint_t               i;
    ngx_msec_t               now;
    int                      found = 0;

    tbl = key_table();
    if (tbl == NULL || key == NULL || key[0] == '\0') {
        return 0;
    }

    now = ngx_current_msec;

    ngx_shmtx_lock(&xrootd_tpc_key_mutex);

    for (i = 0; i < XROOTD_TPC_KEY_SLOTS; i++) {
        e = &tbl->slots[i];
        if (!e->in_use) {
            continue;
        }
        if (xrootd_shm_slot_expired(now, e->expiry)) {
            ngx_memzero(e, sizeof(*e));
            continue;
        }
        if (tpc_key_eq(e->key, key)) {
            found = 1;
            break;
        }
    }

    ngx_shmtx_unlock(&xrootd_tpc_key_mutex);
    return found;
}

/*
 *
 * WHAT: Consumes (destroys) a TPC key from the registry. Unlike validate which only checks existence,
 *       consume permanently removes the key entry after finding it — the slot is zeroed and marked as
 *       unused. This one-time-use semantics ensures each transfer key can be used exactly once for
 *       authentication before being discarded.
 *
 * WHY: TPC keys are single-use credentials. Once a destination server has successfully connected and
 *       fetched data, the key must be consumed to prevent replay attacks or stale connections from
 *       reusing old rendezvous identifiers against new transfers. */
int
xrootd_tpc_key_consume(const char *key)
{
    xrootd_tpc_key_table_t *tbl;
    xrootd_tpc_key_entry_t *e;
    ngx_uint_t               i;
    ngx_msec_t               now;
    int                      found = 0;

    tbl = key_table();
    if (tbl == NULL || key == NULL || key[0] == '\0') {
        return 0;
    }

    now = ngx_current_msec;

    ngx_shmtx_lock(&xrootd_tpc_key_mutex);

    for (i = 0; i < XROOTD_TPC_KEY_SLOTS; i++) {
        e = &tbl->slots[i];
        if (!e->in_use) {
            continue;
        }
        if (xrootd_shm_slot_expired(now, e->expiry)) {
            ngx_memzero(e, sizeof(*e));
            continue;
        }
        if (tpc_key_eq(e->key, key)) {
            ngx_memzero(e, sizeof(*e));
            found = 1;
            break;
        }
    }

    ngx_shmtx_unlock(&xrootd_tpc_key_mutex);
    return found;
}

/*
 *
 * WHAT: Removes a TPC key from the registry without checking TTL or expiration. Unlike consume which
 *       requires the key to be active, remove works on any entry regardless of state — this is useful
 *       for cleanup operations where expired keys should also be removed.
 *
 * WHY: Provides explicit removal capability beyond automatic expiry management. Useful when a transfer
 *       fails mid-flight and operators want to force-remove the rendezvous key rather than waiting for
 *       TTL expiration. Also helps during shutdown or configuration reload scenarios. */
void
xrootd_tpc_key_remove(const char *key)
{
    xrootd_tpc_key_table_t *tbl;
    xrootd_tpc_key_entry_t *e;
    ngx_uint_t               i;

    tbl = key_table();
    if (tbl == NULL || key == NULL || key[0] == '\0') {
        return;
    }

    ngx_shmtx_lock(&xrootd_tpc_key_mutex);

    for (i = 0; i < XROOTD_TPC_KEY_SLOTS; i++) {
        e = &tbl->slots[i];
        if (e->in_use && tpc_key_eq(e->key, key)) {
            ngx_memzero(e, sizeof(*e));
            break;
        }
    }

    ngx_shmtx_unlock(&xrootd_tpc_key_mutex);
}
