#!/usr/bin/env bash
# Entrypoint for the official XRootD reference server container.
#
# Environment variables (all optional — sensible defaults shown):
#
#   AUTH_MODE       anonymous | gsi | token | both   (default: anonymous)
#   DATA_DIR        Filesystem root exported by XRootD  (default: /data/xrootd)
#   XROOTD_PORT     Listener port                        (default: 1094)
#   SERVER_CERT     Path to host EE cert (GSI/token)     (default: /etc/grid-security/hostcert.pem)
#   SERVER_KEY      Path to host private key             (default: /etc/grid-security/hostkey.pem)
#   CA_DIR          Directory of trusted CA certificates (default: /etc/grid-security/certificates)
#   AUTHDB_PATH     Path to xrootd acc.authdb file       (default: /etc/xrootd/authdb)
#   SCITOKENS_CFG   Path to scitokens config             (default: /etc/xrootd/scitokens.cfg)
#   JWKS_URL        URL of the JWKS endpoint for token validation
#                   (written into scitokens.cfg if SCITOKENS_CFG does not exist)
#   TOKEN_ISSUER    Expected token issuer (iss claim)    (default: https://test.xrootd.local)
#   TOKEN_AUDIENCE  Expected token audience              (default: xrootd-test)
set -euo pipefail

AUTH_MODE="${AUTH_MODE:-anonymous}"
DATA_DIR="${DATA_DIR:-/data/xrootd}"
XROOTD_PORT="${XROOTD_PORT:-1094}"
SERVER_CERT="${SERVER_CERT:-/etc/grid-security/hostcert.pem}"
SERVER_KEY="${SERVER_KEY:-/etc/grid-security/hostkey.pem}"
CA_DIR="${CA_DIR:-/etc/grid-security/certificates}"
AUTHDB_PATH="${AUTHDB_PATH:-/etc/xrootd/authdb}"
SCITOKENS_CFG="${SCITOKENS_CFG:-/etc/xrootd/scitokens.cfg}"
TOKEN_ISSUER="${TOKEN_ISSUER:-https://test.xrootd.local}"
TOKEN_AUDIENCE="${TOKEN_AUDIENCE:-xrootd-test}"

mkdir -p "$DATA_DIR"

# ---------------------------------------------------------------------------
# Seed data directory with a handful of test files if it is empty.
# ---------------------------------------------------------------------------
if [ -z "$(ls -A "$DATA_DIR")" ]; then
    echo "Seeding $DATA_DIR with test fixture files…"
    echo "xrootd integration test data" > "${DATA_DIR}/hello.txt"
    dd if=/dev/urandom bs=1M count=10 2>/dev/null \
        | base64 > "${DATA_DIR}/random-10mb.b64"
    mkdir -p "${DATA_DIR}/subdir"
    echo "subdir file" > "${DATA_DIR}/subdir/nested.txt"
fi

# ---------------------------------------------------------------------------
# Build hash symlinks in the CA directory so GSI can find the trusted CA.
# ---------------------------------------------------------------------------
if [ -d "$CA_DIR" ] && command -v openssl &>/dev/null; then
    for cert in "${CA_DIR}"/*.pem "${CA_DIR}"/*.crt; do
        [ -f "$cert" ] || continue
        hash=$(openssl x509 -noout -hash -in "$cert" 2>/dev/null) || continue
        ln -sf "$(basename "$cert")" "${CA_DIR}/${hash}.0" 2>/dev/null || true
    done
fi

# ---------------------------------------------------------------------------
# Generate a scitokens.cfg if one was not mounted.
# ---------------------------------------------------------------------------
if { [ "$AUTH_MODE" = "token" ] || [ "$AUTH_MODE" = "both" ]; } \
        && [ ! -f "$SCITOKENS_CFG" ]; then
    JWKS_URL="${JWKS_URL:-http://jwks-server:8080/jwks.json}"
    cat > "$SCITOKENS_CFG" <<EOF
[Global]
audience = ${TOKEN_AUDIENCE}
[Issuer ${TOKEN_ISSUER}]
issuer = ${TOKEN_ISSUER}
base_path = /
default_user = xrootd
map_subject = false
# For local testing we fetch the JWKS from the in-cluster JWKS server.
EOF
    echo "Generated ${SCITOKENS_CFG} (issuer=${TOKEN_ISSUER})"
fi

# ---------------------------------------------------------------------------
# Write a minimal authdb if one was not mounted.
# ---------------------------------------------------------------------------
if [ ! -f "$AUTHDB_PATH" ]; then
    # Allow the test user DN full read/write access.
    TEST_DN="${AUTH_DN:-/DC=test/DC=xrootd/CN=Test User}"
    cat > "$AUTHDB_PATH" <<EOF
# Generated by xrootd-reference entrypoint.sh
# Format: <auth-type> <identity> <access-level> <path>
g /DC=test/DC=xrootd/** /
u xrootd / / rw /
EOF
    echo "Generated ${AUTHDB_PATH}"
fi

# ---------------------------------------------------------------------------
# Select the xrootd config that matches AUTH_MODE.
# ---------------------------------------------------------------------------
case "$AUTH_MODE" in
    anonymous) CFG=/etc/xrootd/xrootd-anon.cf  ;;
    gsi)       CFG=/etc/xrootd/xrootd-gsi.cf   ;;
    token)     CFG=/etc/xrootd/xrootd-token.cf ;;
    both)      CFG=/etc/xrootd/xrootd-both.cf  ;;
    *)
        echo "ERROR: Unknown AUTH_MODE '${AUTH_MODE}'. Use: anonymous gsi token both" >&2
        exit 1
        ;;
esac

# ---------------------------------------------------------------------------
# Inline variable substitution into the chosen config file.
# ---------------------------------------------------------------------------
RENDERED=/tmp/xrootd-rendered.cf
sed \
    -e "s|{PORT}|${XROOTD_PORT}|g" \
    -e "s|{DATA_DIR}|${DATA_DIR}|g" \
    -e "s|{SERVER_CERT}|${SERVER_CERT}|g" \
    -e "s|{SERVER_KEY}|${SERVER_KEY}|g" \
    -e "s|{CA_DIR}|${CA_DIR}|g" \
    -e "s|{AUTHDB_PATH}|${AUTHDB_PATH}|g" \
    -e "s|{SCITOKENS_CFG}|${SCITOKENS_CFG}|g" \
    "$CFG" > "$RENDERED"

echo "=== XRootD reference server starting ==="
echo "  auth mode : $AUTH_MODE"
echo "  config    : $CFG → $RENDERED"
echo "  data dir  : $DATA_DIR"
echo "  port      : $XROOTD_PORT"
cat "$RENDERED"
echo "========================================"

# Ensure hostkey has the permissions xrootd expects.
[ -f "$SERVER_KEY" ] && chmod 400 "$SERVER_KEY" || true

exec xrootd -c "$RENDERED" -n xrootd -l /dev/stderr
